<h1 class="font-weight-bold" style="font-size:24px !important; color: #2f308a;padding-bottom:2px;">多功能且灵活</h1>

<p class = "text-justify" style="font-size:16px;">
蛋白胨是用途广泛的疫苗开发解决方案，可用于饲料、补充剂、血清还原或替代品。它们源自动物、植物和微生物，具有多种营养成分，可用于各种工艺类型、细胞类型和病毒滴度。它们高度稳定，保质期长，可以轻松混合到任何培养基或补充剂中，与血清相比，具有更大的灵活性。
</p>



<h1 class="font-weight-bold" style="font-size:24px !important; color: #2f308a;padding-bottom:2px;">提高流程生产率</h1>

<p class = "text-justify" style="font-size:16px;">
开发疫苗需要优化细胞培养基、补料和血清替代品，以降低成本并提高工艺生产率。蛋白胨可提高性能、缩短开发时间并维持病毒滴度。
</p>



<h1 class="font-weight-bold" style="font-size:24px !important; color: #2f308a;padding-bottom:2px;">监管机构和供应商都熟悉蛋白胨</h1>

<p class = "text-justify" style="font-size:16px;">
市场上有许多疫苗在制造过程中使用蛋白胨，包括乙型肝炎疫苗。因此，供应商和监管机构在使用蛋白胨进行疫苗开发方面都有丰富的经验。例如，“GibcoTM”蛋白胨目前用于制造 150 多种上市药物，其中包括 15 种重磅药物以及更多正在研发中的药物。最终，监管机构的这种熟悉程度应有助于简化疫苗备案流程。
</p>


<p class = "text-justify" style="font-size:16px;">
虽然与血清相比，蛋白胨具有许多优点，但并非所有蛋白胨都是相同的，并且在整个生物过程中会提供不同的优点。历史上，蛋白胨是为食品工业制造的营养添加剂和风味增强剂。
</p>


<p class = "text-justify" style="font-size:16px;">
在考虑蛋白胨时，选择专门为生物加工应用制造和测试的蛋白胨对于保持批次之间的一致性至关重要。
</p>


