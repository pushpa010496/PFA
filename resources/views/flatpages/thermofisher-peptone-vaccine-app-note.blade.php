<h1 class="font-weight-bold" style="font-size:24px !important; color: #2f308a;padding-bottom:2px;">Versatile and flexible</h1>

<p class = "text-justify" style="font-size:16px;">
Peptones are versatile vaccine development solutions used in feeds, supplements, serum reduction, or replacement. Originating from animals, plants, and microbial sources, they have diverse nutritional profiles and can be used for various process types, cell types, and viral titers. They are highly stable, have a long shelf life, and can easily blend into any medium or supplement, providing more flexibility compared to serum.
</p>



<h1 class="font-weight-bold" style="font-size:24px !important; color: #2f308a;padding-bottom:2px;">Improve process productivity</h1>

<p class = "text-justify" style="font-size:16px;word-spacing:1px;">
Developing a vaccine requires optimizing cell culture media, feeds, and serum substitutes to reduce costs and increase process productivity. Peptones enhance performance, reduce development timelines, and sustain viral titers.
</p>


<h1 class="font-weight-bold" style="font-size:24px !important; color: #2f308a;padding-bottom:2px;">Peptones are familiar to regulators and suppliers</h1>

<p class = "text-justify" style="font-size:16px;">
There are numerous vaccines on the market that utilize
peptones in their manufacture, including the hepatitis B
vaccine. As a result, both suppliers and regulators are
experienced in the use of peptones for vaccine development.
 "Gibco<span class="small" style="font-size:10px;"><sup>TM</sup></span>" peptones, for instance, are currently used in the
manufacture of more than 150 marketed drugs, including
15 blockbuster drugs and many more in the pipeline.
Ultimately, this familiarity from regulators should help
streamline the vaccine filing process.
</p>

<p class = "text-justify" style="font-size:16px;word-spacing:1px">
While peptones offer many benefits when compared with
sera, not all peptones are created equal and will offer
different benefits across bioprocesses. Historically,
peptones were manufactured for the food industry
as nutritional additives and flavor enhancers. 
</p>

