</p>
<h1 class="font-weight-bold" style="font-size:24px !important;padding-bottom:3px; color:#2f308a;">혈청의 대안</h1>

<p class = "text-justify" style="font-size:16px;">혈청은 포유류 세포 배양 생산에 사용되지만 가용성 제한, 공급 변동, 비용 변동, 긴 주문 프로세스 등의 문제로 인해 백신 개발자는 대안을 모색하게 됩니다.</p>




<h1 class="font-weight-bold" style="font-size:24px !important; color: #2f308a;padding-bottom:2px;">다재다능하고 유연함</h1>

<p class = "text-justify" style="font-size:16px;">
펩톤은 사료, 보충제, 혈청 감소 또는 대체에 사용되는 다목적 백신 개발 솔루션입니다. 동물, 식물 및 미생물 소스에서 유래한 이 제품은 다양한 영양 프로필을 가지며 다양한 공정 유형, 세포 유형 및 바이러스 역가에 사용할 수 있습니다. 이 제품은 매우 안정적이고 유통기한이 길며 모든 배지나 보충제에 쉽게 혼합될 수 있어 혈청에 비해 더 많은 유연성을 제공합니다.
</p>



<h1 class="font-weight-bold" style="font-size:24px !important; color: #2f308a;padding-bottom:2px;">공정 생산성 향상</h1>

<p class = "text-justify" style="font-size:16px;">

백신을 개발하려면 비용을 절감하고 공정 생산성을 높이기 위해 세포 배양 배지, 사료 및 혈청 대체품을 최적화해야 합니다. 펩톤은 성능을 향상시키고, 개발 일정을 단축하며, 바이러스 역가를 유지합니다.

</p>




<h1 class="font-weight-bold" style="font-size:24px !important; color: #2f308a;padding-bottom:2px;">펩톤은 규제 기관과 공급업체에게 친숙합니다.</h1>

<p class = "text-justify" style="font-size:16px;">
B형 간염 백신을 포함하여 제조 시 펩톤을 활용하는 수많은 백신이 시중에 나와 있습니다. 결과적으로 공급업체와 규제 기관 모두 백신 개발을 위한 펩톤 사용에 대한 경험이 있습니다. 예를 들어, "GibcoTM" 펩톤은 현재 15개 블록버스터 약물과 파이프라인에 있는 더 많은 약물을 포함하여 150개 이상의 시판 약물 제조에 사용됩니다. 궁극적으로 규제 당국의 이러한 친숙함은 백신 제출 프로세스를 간소화하는 데 도움이 될 것입니다.
</p>


<p class = "text-justify" style="font-size:16px;">
