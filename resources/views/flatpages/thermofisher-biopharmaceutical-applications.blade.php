molecule drugs. Liquid chromatography (LC) coupled with mass

spectrometry (MS), LC-MS, has become increasingly popular for

characterizing biopharmaceutical drugs. LC-MS is widely used

for characterizing the primary structure of protein drugs such as

amino acid sequencing for peptides/proteins, profiling of

process-related impurities such as host cell proteins (HCPs),

disulfide bond mapping, N-glycan profiling of monoclonal

antibodies (mAbs), and nucleotide sequencing for

oligonucleotides.



                </p>

