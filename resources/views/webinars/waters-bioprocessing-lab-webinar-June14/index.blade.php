							<h2 class="font-weight-bold TXT-Blue mb-3" style="font-size:24px;">Performing Product Quality Measurements in your Bioprocessing Lab – It's Easier Than You Think</h2>
								<p>
								This webinar will illustrate how you can perform product and process quality measurements on cell cultures from the Sartorius Ambr® 250 High Throughput Bioreactor System by using the Waters BioAccord™ LC-MS System. You will learn how to take samples during upstream PD studies and rapidly perform advanced analytics including intact mass, subunit analyses, and media monitoring. These can be added to your existing daily measurements and allows you to have all this information directly at your fingertips.
								</p>
								The Ambr® 250 system is a high throughput, automated bioreactor system for process development with 12 or 24 fully featured single-use 100 – 250 mL mini bioreactors. The completely integrated high throughput system with easy-connect bioreactors and flexible software enables scientists to manage experiments in parallel while reducing the cost per experiment. Ambr® 250 High Throughput is ideal for scaling down processes and provides a step change improvement in lab productivity. 
							</p>
							<p>
							The Waters BioAccord™ LC-MS System enables direct measurement of multiple critical process and product attributes including identity, purity, and glycosylation. In this webinar, we will show you how to add intact glycoprofiling and subunit drug product quality measurements along with spent media profiling to your routine monitoring workflows for process development. In addition to reducing turnaround time from sampling to results, we will highlight how to minimize the burden of sample preparation through automation.
							</p>

