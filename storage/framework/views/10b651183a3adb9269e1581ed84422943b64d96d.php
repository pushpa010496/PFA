							<p>This webinar will walk through the process and benefits implementing the use of Total Organic Carbon (TOC) in Pharmaceutical. We will talk through the use of TOC analysis in Pharmaceutical water, for performing Cleaning Validation, Replacing HPLC with TOC for CV, for checking the extractable and leachable organics from Plastics used in packaging of pharmaceutical products.</p>

							<h3 class="font-weight-bold">Synopsis:</h3>
							<p class="mb-1">With a steadily increasing number of recombinant proteins that are being developed as therapeutics there is an increasing demand to provide very exacting methods for validating the product. Unlike their small molecule counterparts there are many more analytical steps – and with them come more analytical challenges –involved in showing that the correct products have been made. Proteins can undergo a myriad of modifications each of which can have a profound effect on the efficacy and half-life of the protein. Some of these modifications are required and some are not, however all of them can vary depending on the growth conditions of the cell line that is being used to express them. Early process development focuses on the conditions of cell growth that provides the protein of the correct amino acid sequence and with the correct, intentional modifications (e.g., glycosylation, disulfide-bond linkages). As this is scaled up it is vital to show that the fidelity of the product remains the same. Once this has been achieved then there will be the need for routine batch testing to provide evidence of reproducibility and batch-to-batch consistency.</p>

							<p>One of the most widely used and highly applicable analytical techniques as part of the characterization is peptide mapping. This approach involves the isolation of the protein, controlled chemical modification, enzymatic digestion, separation of the resultant peptides and detection. Using this approach, it is possible to determine that the correct amino acid sequence has been obtained, identify many of the modifications (required and erroneous), all semi-quantitatively.</p>

							<p>This webinar will describe the new approaches and technologies that are being used in each of the steps and how an efficient, reproducible and accurate workflow can be obtained.</p>
