						<h2 class="font-weight-bold PF-TXTAQUA font-18">Cell Culture Bioprocess Scale-Up Workflow from Bench to Pilot/Production Scale using Single-Use Bioreactors</h2>
						
						<p class="mb-1">Transferring a biologic candidate from the research and development phase to commercial production usually requires increasing the working volume of the upstream bioprocess. During scale-up, process performance optimized at small scale needs to be reproduced at larger scales, ideally without much need for process optimization at large working volumes. This requires reproducing the quality of the seed culture and the cells’ growth environment across scales.</p>

						<p class="mb-1">To streamline scale-up, bioengineers commonly use bioreactors with similar geometries at all scales and keep one or more parameters constant between vessels of different sizes, such as kLa, power input per volume or tip speed. Therefore, to design a scale-up strategy, data on scale-up-relevant engineering parameters need to be available for each bioreactor planned to be used. These parameters include vessel geometries, power numbers, and oxygen transfer capabilities.</p>

						<p>In this webinar we showcase scale-up of an antibody production process in CHO cells from bench to pilot/production scale. We discuss strategies to prepare seed cultures of reproducible quality in shake flasks. We demonstrate a new concept for software-aided scale-up strategy design, which considers vessel parameters of differently sized Eppendorf BioBLU® Single-Use Vessels and Thermo™ HyPerforma™ Single-Use Bioreactors</p>

						<p class="font-weight-bold PF-TXTAQUA font-20">Learning objectives:</p>

						<ul style="padding-left: 20px; line-height: 1.3;">
