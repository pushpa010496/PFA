Direct LC-MS Characterization of Glycoform Distribution and Low Molecular Weight Impurities in mAb Process From Ambr® 15 Bioreactors
</h3>

<!-- <h3 class="font-weight-bold" style="color: #000; font-size: 18px;">Abstract / Summary</h3> -->
<p class = "text-justify" style="font-size:17px;">
Full characterization of Critical Quality Attributes (CQAs) in biotherapeutic proteins during upstream development is often limited to a handful of high-performing cultures due to high instrument demand and consequently long core lab turn-around times. Although protein A purification or other affinity enrichment approaches are frequently used for LC-MS characterization of biotherapeutic proteins, elimination of this step reduces sample preparation complexity and time from sampling to analysis.
</p>
<p class = "text-justify" style="font-size:17px;">
In this application note, we demonstrated a streamlined and robust mAb product monitoring method. The mAb fermentation samples were collected directly from an Ambr® 15 running a CHO process. The analytical method was developed on a small bench top LC-MS system, for rapid mAb glycoform confirmations and mAb purity assessments. This developed method is high throughput capable and has the potential to be a fully automated platform for mid-process intraday monitoring of CQAs.
